    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $product = Product::findOrFail($id);
        $tags = implode(',', $product->tags()->pluck('name')->toArray());


        return view('dashboard.products.edit', compact('product', 'tags'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Product $product)
    {
        $product->update($request->except('tags'));

        $tags = json_decode($request->tags);

        $tag_ids = [];
        foreach ($tags as $item) {
            $slug = Str::slug($item->value);
            $tag = Tag::where('slug', $slug)->first(); /// if that tag exesting
            if (!$tag) {
                $tag = Tag::create([
                    'name' => $item->value,
                    'slug' => $slug,
                ]);
            }
            $tag_ids[] = $tag->id;  ///// if tag new or exesting just add its id in array in pass it yo sync function
        }

        $product->tags()->sync($tag_ids);

        return redirect()->route('dashboard.products.index')->with('success', 'Product Updated');
    }

